# Faça um programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

largura_quadrado = float(input('Informe a largura do quadrado: '))
area_quadrado = largura_quadrado * largura_quadrado
dobro_area = area_quadrado * 2
print(f'A área do quadrado é igual a {area_quadrado} m2')
print(f'O dobro da área do quadrado é igual a {dobro_area} m2')
